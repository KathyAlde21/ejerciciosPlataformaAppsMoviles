package Modulo_2.videoTiposDeDatos;

public class TiposDeDatos {
    public static void main(String[] args) {
        
/*      VALOR MAXIMO QUE PUEDE ALMACENAR UNA VARIABLE       */
       //pueden ser negativo y positivo
        byte variableByte = 127;
        
        short variableShort = 32767;
        
        int variableInt = 2147483647;
        
        long variableLong = 9223372036854775807L; //defe finalizar siempre con L mayuscula
        
        float variableFloat = 3.4028234664E+38F; //variables con decimales
        
        double variableDouble = 1.7976931348623157E308; //variables con decimales
        
        
   
/*      EN CASO QUE OLVIDE ESE VALOR ESCRIBO ESTO Y EN CONSOLA MUESTRA VALOR        */
        System.out.println(Double.MAX_VALUE);    //primera letra
        
        
    }
 
}
